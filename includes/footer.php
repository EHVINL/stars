    <script>
        // Inicializar Feather Icons
        document.addEventListener('DOMContentLoaded', function() {
            // Feather Icons
            if (typeof feather !== 'undefined') {
                feather.replace();
            }
            
            // AOS Animation
            if (typeof AOS !== 'undefined') {
                AOS.init({
                    duration: 800,
                    easing: 'ease-in-out',
                    once: true,
                    offset: 100
                });
            }
            
            // Loading Spinner
            const loading = document.getElementById('loading');
            if (loading) {
                setTimeout(() => {
                    loading.style.opacity = '0';
                    setTimeout(() => {
                        loading.style.display = 'none';
                    }, 300);
                }, 1000);
            }
            
            // Mobile Menu Toggle
            const mobileMenuButton = document.getElementById('mobileMenuButton');
            const mobileMenu = document.getElementById('mobileMenu');
            
            if (mobileMenuButton && mobileMenu) {
                mobileMenuButton.addEventListener('click', function() {
                    mobileMenu.classList.toggle('open');
                    const icon = mobileMenuButton.querySelector('i');
                    if (mobileMenu.classList.contains('open')) {
                        icon.setAttribute('data-feather', 'x');
                    } else {
                        icon.setAttribute('data-feather', 'menu');
                    }
                    feather.replace();
                });
                
                // Fechar menu ao clicar fora
                document.addEventListener('click', function(e) {
                    if (!mobileMenu.contains(e.target) && !mobileMenuButton.contains(e.target)) {
                        mobileMenu.classList.remove('open');
                        const icon = mobileMenuButton.querySelector('i');
                        icon.setAttribute('data-feather', 'menu');
                        feather.replace();
                    }
                });
            }
            
            // Back to Top Button
            const backToTop = document.getElementById('backToTop');
            if (backToTop) {
                window.addEventListener('scroll', function() {
                    if (window.pageYOffset > 300) {
                        backToTop.style.opacity = '1';
                        backToTop.style.transform = 'translateY(0)';
                    } else {
                        backToTop.style.opacity = '0';
                        backToTop.style.transform = 'translateY(10px)';
                    }
                });
                
                backToTop.addEventListener('click', function() {
                    window.scrollTo({
                        top: 0,
                        behavior: 'smooth'
                    });
                });
            }
            
            // Auto-hide Flash Messages
            const flashMessage = document.querySelector('[class*="bg-green-500"]');
            if (flashMessage) {
                setTimeout(() => {
                    flashMessage.style.opacity = '0';
                    flashMessage.style.transform = 'translateX(100%)';
                    setTimeout(() => {
                        flashMessage.remove();
                    }, 300);
                }, 5000);
            }
            
            // Smooth Scroll para Links Internos
            document.querySelectorAll('a[href^="#"]').forEach(anchor => {
                anchor.addEventListener('click', function (e) {
                    e.preventDefault();
                    const target = document.querySelector(this.getAttribute('href'));
                    if (target) {
                        target.scrollIntoView({
                            behavior: 'smooth',
                            block: 'start'
                        });
                    }
                });
            });
            
            // Newsletter Form Validation
            const newsletterForm = document.querySelector('footer form');
            if (newsletterForm) {
                newsletterForm.addEventListener('submit', function(e) {
                    e.preventDefault();
                    const email = this.querySelector('input[type="email"]').value;
                    
                    if (email && isValidEmail(email)) {
                        // Simular envio
                        this.innerHTML = `
                            <div class="text-center text-white">
                                <i data-feather="check-circle" class="w-8 h-8 text-green-400 mx-auto mb-2"></i>
                                <p class="font-medium">Inscrito com sucesso!</p>
                                <p class="text-sm text-purple-200">Obrigado por se inscrever.</p>
                            </div>
                        `;
                        feather.replace();
                    }
                });
            }
            
            // Função de validação de email
            function isValidEmail(email) {
                const re = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
                return re.test(email);
            }
            
            // Adicionar classe active baseado na URL atual
            function setActiveNavLinks() {
                const currentPage = window.location.pathname.split('/').pop();
                document.querySelectorAll('.nav-link').forEach(link => {
                    const linkPage = link.getAttribute('href');
                    if (linkPage === currentPage || 
                        (currentPage === '' && linkPage === 'home.php') ||
                        (linkPage.includes('admin/') && window.location.pathname.includes('admin/'))) {
                        link.classList.add('active');
                    }
                });
            }
            
            setActiveNavLinks();
        });
        
        // Prevenir formulários de enviar múltiplas vezes
        document.addEventListener('submit', function(e) {
            const form = e.target;
            const submitButton = form.querySelector('button[type="submit"], input[type="submit"]');
            
            if (submitButton) {
                submitButton.disabled = true;
                submitButton.innerHTML = `
                    <div class="flex items-center space-x-2">
                        <div class="w-4 h-4 border-2 border-white border-t-transparent rounded-full animate-spin"></div>
                        <span>Processando...</span>
                    </div>
                `;
                
                // Re-enable after 5 seconds (fallback)
                setTimeout(() => {
                    submitButton.disabled = false;
                    submitButton.innerHTML = submitButton.getAttribute('data-original-text') || 'Enviar';
                }, 5000);
            }
        });
    </script>
